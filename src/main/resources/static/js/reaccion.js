
                            function mostrarIconos() {

                                /* abre o cierra el tablero de iconos */
                                text = document.getElementById("con");
                                if(text.style.display == "none" || text.style.display == "") {
                                    text.style.display = "block";
                                } else {
                                    text.style.display = "none";
                                }
                            }